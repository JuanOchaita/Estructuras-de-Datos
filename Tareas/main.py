def convert_to_honk(input_string):
    if len(input_string) == 0:
        return ''
    elif len(input_string) == 1:
        return 'H'
    elif len(input_string) == 2:
        return 'Hn'
    result = ['H']
    middle_length = len(input_string) - 2
    result.extend(['o'] * (middle_length - 1))
    result.append('n')
    result.append('k')
    return ''.join(result)


